class obj {

/*******************************************************************************
*                               Public methods                                 *
*******************************************************************************/
    function __construct($obv){
        foreach ($obv as $clave => $valor) {
            $this->setCampo($clave,$valor); 
        }
    }
/*******************************************************************************
*                     Retorna el valor del campo invocado                      *
*******************************************************************************/
    function getCampo($campo)
    {
        return $this->$campo;
    }
/*******************************************************************************
*                    Setea el valor del campo con el valor                     *
*******************************************************************************/
    function setCampo($campo,$valor)
    {
        $this->$campo = $valor;
    }
/*******************************************************************************
*                Retorna el valor del campo invocado en String                 *
*******************************************************************************/
    function getStrCampo($campo)
    {
        echo $this->getCampo($campo);
    }
/*******************************************************************************
*                        Retorna el valor del campos                           *
*******************************************************************************/
    function getStrCampos(){
    	$a=null;
        foreach ($this as $clave => $valor) {
        	$a=$a.$this->getCampo($clave)." ";
            
        }
        $a=substr($a, 0, -1);
        return $a;
    }
/*******************************************************************************
*                         Imprime los campos del obj                           *
*                          lo mismo q var_dum()                                *
*******************************************************************************/
    function printCampos() {
       foreach ($this as $clave => $valor) {
           print "$clave: $valor\n";
       }
    }
}
